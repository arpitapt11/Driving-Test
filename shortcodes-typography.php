                            <h1 class="title-post-page">Eligibility</h1>

                            <p> <li> 16 years of age is eligible to get a license for driving a Motor Cycle without Gear - Not exceeding 50 cc.</li>
<li> 18 years of age is eligible to get a license for driving a Motor Cycle with Gear and Light Motor Vehicle.</li> 
<li> 20 years of age is eligible to get a license for driving a Transport Vehicle, if he/she has One year’s
<li> Also, no person shall be granted a learner’s licence to drive a medium or heavy goods vehicle or passenger vehicle unless he has held a driving licence valid for a light motor vehicle (LMV) for at least one year.</li> </p>

                            <h1>Instructions</h1>

                            <p>By having a LLR, a person can drive the bike in main road with the presence of driving license holder in driving bike. The validity of the LLR is 6 months before that he/she should take driving license. If the LLR is expired he/she should put new LLR to get the license. After one month of learning a driving he/she should apply for license to rto by getting the driving licence form. After the application, the rto will call for a test to get a license.<p>

                            <h1>Required Information</h1>

                            <p>Full name of the Applicant
<li>Father's Name</li>
<li>Permanent Address / Temporary Address</li>
<li>Date of Birth</li>
<li>Place of Birth</li>
<li>Education Qualification</li>
<li>Identification Marks</li>
<li>Blood Group</li>

                            </p>
                            
                            <h1>Procedure</h1>
                            <h3>Walk-In Method</h3>

                            <p>For obtaining a Learner’s Licence, you will need to apply in the prescribed format to the Local Transport Office in your region, along with your passport-sized photographs, proof of your age and residence, declaration of medical fitness and the required fee. After verification of your documents, you will have to go through the Learner’s Test. Usually a handbook of traffic rules, signs and regulations is provided with the application form. On passing the Learner’s Test, you will be issued a Learner’s Licence. If you fail the test, you will be given a chance to take the test again.</p>
                            
                            <h3>Online Method</h3>

                            <p><li>Go to the RTO Appointment site Link</li>
<li>For some RTOs the site is (https://parivahan.gov.in/sarathiservice/newLLDet.do Link). If you choose the RTO you will be told to use this site.</li>
<li>Please click on the link "Learner's license online Application" which would then take you to the form.</li>
<li>Fill out the details requested in the form</li>
<li>Select RTO Jurisdiction district and RTO office.</li>
<li>In Transport type specify Learner’s license.</li>
<li>Then select the suitable date of appointment and check availability. Once the date is chosen. Enter the authentication code in the box.</li>
<li>For Leaner's Driving License you need to fill out Form1.</li>
<li>Press Submit.
<li>Then approach the RTOs chosen for getting LLRs and take the Learner's Test, after passing the tests you will be issued a Learner’s Licence. If you fail the test, you will be given a chance to take the test again.</li></p>
                            
                            <h1>Processing Time</h1>

                            <p>After completion of Learner's test, the Licence will be issued on the same day or within a weeks time.</p>

                            
